<h1>QUIENES <span class="negro">SOMOS</span></h1>
<div class="qs_txt">
 <img src="images/quienessomos/1.jpg" width="391" height="297" style="float: right; margin: 5px 0px 0px 15px; border: 2px solid #c80600;" />
<p>El 12 de agosto de 1990 se constituye la sociedad INDUSTRIAS METALURGICAS INMETAL Ltda., empresa dedicada principalmente a la fabricación de piezas en aceros, bronces, hierros y aluminios. Nuestro compromiso con la calidad y el servicio al cliente nos ha ayudado a sostener relaciones duraderas con nuestros clientes.
</p>
  <p>Somos una empresa, que cuenta con más de veinte años al servicio de la industria colombiana, y nuestro compromiso es elaborar productos de excelente calidad.</p>
  <p> Para satisfacer los requerimientos de nuestros clientes, aplicamos a nuestros productos los controles de aseguramiento de la calidad y normatividad de los mismos, disponiendo para su logro de un laboratorio de espectrometría y metalografía, además de contar con ingenieros capacitados y con experiencia en el área de la fundición.</p>
  <p> En Mayo de 2012 recibimos el premio “GOLD AWARD FOR EXCELLENCE AND BUSINESS PRESTIGE” otorgado por Business Initiative Directions durante la celebración del Quality Summit New York 2012, como reconocimiento  a la búsqueda continua de la calidad. </p>
