    	<h1>Políticas de calidad</h1>
      <div class="misc-tx">
      	<p>En Terra Capital Group se desarrollan procesos administrativos y técnicos bajo  estrictos requerimientos de normas de calidad.</p>
        <p>Se utilizan insumos y se aplican métodos de producción, almacenamiento y distribución adecuados para garantizar a lo largo de la cadena de valor agropecuaria la preservación de la calidad planeada.</p>
        <p>Al ser este un ciclo de negocio complejo, el cual incluye un sin múmero de porcesos sucesivos que empiezan desde la compra de materias primas hasta el consumo final, se compran materias primas de la mejor calidad y se desarrollan las mejores condiciones laborales en todos los sectores que se cubren; agrícolas, pecuarias y ambientales incluyendo los procesos de producción, almacenamiento y distribución, para así lograr entregarle al cliente o consumidor un producto que represente nuestra promesa de calidad.</p>
        <p>Se desarrolla en conjunto con nuestros colaboradores, proveedores, distribuidores y clientes unas sólidas relaciones de negocios, para contribuir al éxito común durante el proceso de satisfacer las expectativas y necesidades de clientes y consumidores.</p>
