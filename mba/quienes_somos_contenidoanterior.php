<h1>Política Integral  HSEQ</h1><br/>
<strong>MBA C.I. Mario Bretón, Arbelaez & Cia S.A.S.</strong> es una empresa dedicada a la comercialización de insumos  petroquímicos para la industria, que busca satisfacer las necesidades de sus clientes a través de un servicio ágil, eficiente y responsable.<br/>
<br/>

Somos una organización proactiva que busca anticiparse a las nuevas tendencias del mercado, asignando los recursos necesarios para el mejoramiento continuo en sus procesos y la fidelización de sus clientes.<br/><br/>

Comprometidos con la entrega de productos y servicios que satisfagan las necesidades y expectativas de nuestros clientes, protegiendo y conservando la salud y seguridad de nuestros trabajadores, previniendo lesiones personales, enfermedades profesionales, daños a la propiedad e impactos socio–ambientales mediante el control y aseguramiento de los peligros y riesgos y los aspectos e impactos que contemplan cada una de nuestras actividades a nivel nacional.<br/><br/>

Para ello, en<strong> MBA C.I. Mario Bretón Arbelaez & Cia  S.A.S,</strong> nos comprometemos a:<br/>
<br/>

• Cumplir con la legislación vigente y aplicable en nuestras operaciones con respecto al Medio ambiente y la Seguridad y Salud Ocupacional con responsabilidad social, y con los demás requisitos que la organización suscriba y se comprometa a cumplir con sus clientes y otras partes interesadas.<br/><br/>

• Mejorar continuamente todos nuestros procesos y servicios identificando y controlando a través de acciones y programas, la satisfacción de nuestros clientes, los riesgos en seguridad y salud ocupacional y los impactos socio- ambientales significativos.<br/><br/>

• Facilitar los recursos económicos necesarios, tecnológicos y humanos para el correcto establecimiento y mantenimiento de nuestro sistema de gestión y de las actividades que se generen del mismo.<br/><br/>

• Mejorar y desarrollar nuestro personal en competencias, especialmente al personal que controla nuestras actividades, compartiendo y exigiendo a nuestros contratistas, con el fin de asegurar y prestar el mejor servicio en el mercado.<br/><br/>

• Establecer y mantener procedimientos, registros, y programas documentados para la realización de actividades críticas identificadas en nuestras operaciones y comunicar dichos procedimientos a nuestros colaboradores, con el fin de prevenir la ocurrencia de accidentes de trabajo, enfermedades profesionales, daños a la propiedad y al ambiente.<br/><br/>

• Fomentar la responsabilidad social con sus grupos de interés: representantes de la organización como socios, propietarios, directivos, aliados y gremios, trabajadores, sus familias y sus organizaciones, clientes y consumidores y sus organizaciones, competidores, estado, comunidades, proveedores, medio ambiente y otros que resulten relevantes para la  organización.<br/><br/>

