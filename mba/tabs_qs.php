		  <p><strong>EXCO COLOMBIANA S.A. </strong>nació como la multinacional <strong>ARMCO COLOMBIANA S.A.</strong>, compañía filial de <strong>ARMCO INC.</strong> , una de las principales acerías de Estados Unidos.</p>
<p>En el año de 1981 se invirtió en el montaje de una planta para la fabricación de tubería mecánica en la ciudad de Cali para comercializar tubería a través de grandes y medianos distribuidores.</p>
<p>En 1985 y como estrategia particular al mercado colombiano, se inició un proceso de diferenciación con respecto a los demás fabricantes de tubería en Colombia, agregando valor al tubo mediante procesos tales como tubería cortada a la medida y, tubería doblada según especificaciones del cliente.
En 1991 la multinacional americana decide vender la operación a inversionistas locales. En enero de 1992 se traslado la planta de manufactura a la ciudad de Pereira, en donde se construyó una edificación a la medida de las necesidades de producción operando con una licencia por dos años y conservando el nombre de<strong> ARMCO COLOMBIANA S.A.</strong>;  después de este tiempo la empresa cambia de nombre por el de <strong>EXCO COLOMBIANA S.A (EXCO = EXARMCO)</strong>. Desde esa fecha la estrategia global de <strong>EXCO COLOMBIANA S.A</strong> se ha encaminado a satisfacer las necesidades de la industria metalmecánica con soluciones tubulares a la medida de nuestros clientes.
Para el logro de esta estrategia, en<strong> Exco Colombiana S.A.</strong> hemos invertido en tecnología de punta en procesamiento de tubería. Es así que el alcance de nuestra oferta en la actualidad vaya desde un tubo de alta especificación hasta estructuras soldadas y pintadas.</p>
