Descripción: Las ceras Parafínica, o sencillamente parafinas, son una mezcla de hidrocarburos saturados (enlaces sencillos entre carbonos) de cadena lineal, cuyo peso molecular oscila entre 320 y 560, y presentan consistencia sólida a temperatura ambiente. Se obtiene a partir de fracciones de la destilación al vacío de crudo reducido (fondos de la destilación atmosférica) con rango de destilación entre 350 y 650 °C, las cuales se someten a procesos de dilución con solvente, enfriamiento regulado, cristalización y filtración para separar las parafinas de los aceites.
